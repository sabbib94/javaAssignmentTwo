package Assignment2;

public class PrimitiveDataType {
    public static void main(String[] args) {
        int myNum=12;
        byte b = 4;
        short s = 56;
        double d = 4.355453532;
        long l = 12121;
        float myFloatNum = 5.99f;
        char myLetter = 'D';
        boolean myBool = true;


        System.out.println("Primitive data type:");
        String myText = "Hello";
        // integer number
        System.out.println("Integer number: "+myNum);
        // byte
        System.out.println("Byte: "+b);
        // Short
        System.out.println("Short: "+s);
        // Double
        System.out.println("Double: "+d);
        // Long
        System.out.println("Long: "+l);
        // floating point number
        System.out.println("Floating point number: "+myFloatNum);
        // character
        System.out.println("character: "+myLetter);
        // boolean type
        System.out.println("Boolean type: "+myBool);
        // String value
        System.out.println("String value: "+myText);
    }
}
